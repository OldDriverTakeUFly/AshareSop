def get_curated_theme_evidence(theme: str | None) -> dict[str, Any] | None:
    if not theme:
        return None
    normalized = theme.strip()
    if not normalized:
        return None
    for pack in CURATED_EVIDENCE_PACKS:
        aliases = {str(alias).strip() for alias in pack.get("aliases", []) if str(alias).strip()}
        if normalized in aliases:
            return pack
    return None


def normalize_theme_with_evidence_pack(theme: str, evidence: dict[str, Any] | None) -> str:
    if evidence and evidence.get("theme"):
        return str(evidence["theme"])
    return theme


def iter_curated_evidence_packs() -> list[dict[str, Any]]:
    return CURATED_EVIDENCE_PACKS
